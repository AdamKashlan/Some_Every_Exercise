// has odd number

function hasOddNumber(arr) {
    return arr.some(function(value) {
        return value % 2 !== 0;
    });
}

// has a zero

function hasAZero(number) {
    let arr = number.toString().split("");
    return arr.some(function(digit) {
        return digit == 0;
    });
}

